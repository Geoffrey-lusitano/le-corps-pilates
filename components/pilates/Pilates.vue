            <h3>Qu'est ce que le Pilates ?</h3>
            <p>C'est une méthode douce ( placement précis, mouvement lent, respiration profonde)
              qui vise à renforcer les chaînes musculaires profondes du corps et redonne de la mobilité à l'ensemble du
              corps.
              Tous les exercices sont observés, adaptés et corrigés afin que chacun puisse progresser rapidement en
              toute sécurité.
              <br>
              <br>
              <strong>Un mot sur JOSEPH PILATES</strong>
              <br>
              La méthode Pilates porte le nom de son créateur, Joseph Pilates, gymnaste, boxeur et plongeur allemand qui
              vécut au début du XXe siècle. Selon lui, tout être humain naît avec un droit universel, le droit de rester
              en bonne santé. À partir de cette idée, il crée un répertoire complet de centaines de mouvements
              spécifiques pour permettre à la colonne vertébrale et aux articulations d’un adulte de s’assouplir. Pour
              lui, cela signifie parvenir à un développement harmonieux du corps, où l’équilibre règne entre le corps et
              les facultés mentales et spirituelles, et l’entretenir.
              <br>
              <br>
              "Après dix séances, vous sentirez la différence, après vingt séances, vous verrez la différence, après
              trente
              séances, vous aurez un corps tout neuf."
              <br>
              <br>
              C’est d’abord le monde de la danse (Martha Graham, Balanchine…) qui comprit l’impact de ses travaux sur le
              mouvement dansé. Joseph Pilates continua d’enseigner jusqu’à sa mort en 1967 et laissa derrière lui
              quelques héritiers. Il fallut attendre que les stars hollywoodiennes découvrent cette technique dans les
              années 1990 pour que le grand public y ait enfin accès.
            </p>
          </div>
